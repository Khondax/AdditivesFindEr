function getValueFromString (text) {
    var textValidPositions = getValidChars(text)
    var name = text.slice(textValidPositions[0], text.indexOf('</', textValidPositions[0]))
    var type = text.slice(textValidPositions[1], text.indexOf('</', textValidPositions[1]))
    var source = text.slice(textValidPositions[2], text.indexOf('</', textValidPositions[2]))
    var additive = text.slice(textValidPositions[3], text.indexOf('</', textValidPositions[3]))
    var clasification = text.slice(textValidPositions[4], text.indexOf('</', textValidPositions[4]))


    return {
        name: name,
        type: type,
        source: source,
        additive: additive,
        clasification: clasification
    }
}

function getValidChars (text) {
    var a=[], i=-1
    while((i = text.indexOf('">', i+1)) > 0) {
        if (text.charAt(i+2) !== '<' && text.charAt(i+2) !== ' ') a.push(i+2)
    }
    return a
}


